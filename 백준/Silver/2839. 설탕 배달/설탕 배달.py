n = int(input())

# dp[i]는 i kg의 설탕을 담기 위해 필요한 최소 봉지의 수입니다.
# 초기에 큰 값을 할당하여 해당 무게의 설탕을 담는 것이 불가능하다고 가정합니다.
dp = [10**9] * (n + 1)

# 3kg과 5kg의 설탕을 담을 수 있으므로, 해당 무게의 봉지 수는 1입니다.
if n >= 3:
    dp[3] = 1
if n >= 5:
    dp[5] = 1

for i in range(6, n + 1):
    # i kg의 설탕을 담기 위한 최소 봉지 수는 (i-3)kg의 설탕을 담는 봉지 수 + 1
    # 또는 (i-5)kg의 설탕을 담는 봉지 수 + 1 중 작은 값입니다.
    dp[i] = min(dp[i-3], dp[i-5]) + 1

# n kg의 설탕을 담기 위한 봉지 수가 초기값(10^9)보다 크면, -1을 출력합니다.
if dp[n] >= 10**9:
    print(-1)
else:
    print(dp[n])
